bool IsGbFile(const std::string& romPath)
{
	return std::filesystem::path(romPath).extension() == ".gb";
}

int Rom::ParseHeader()
{
	for (int i = 0; i < 48; i++) {
		if (data[0x0104 + i] != nintendoLogo[i]) {
			spdlog::error("This ROM is not a genuine GB ROM!");
			return STT_FAILED;
		}
	}

	for (int i = 0; i < 16; i++)
		header.title.push_back(data[0x134 + i]);
	spdlog::info("ROM title: {}", header.title);

	header.romSize = 32 * KiB * (1 << data[0x0148]);
	spdlog::info("Rom size: {} KiB", header.romSize / KiB);

	u8 checksum = 0;
	for (u16 addr = 0x0134; addr <= 0x014C; addr++)
		checksum = checksum - data[addr] - 1;
	if (checksum != data[0x014D]) {
		spdlog::error("Checksum is wrong.");
		return STT_FAILED;
	}

	return STT_SUCCESS;
}

u8 Rom::BootRomRead(u16 addr)
{
	return dmgBootRom[addr];
}

u8 Rom::Read(u16 addr)
{
	u8 ret;

	if (!disableBootROM && addr < 0x0100) {
		ret = BootRomRead(addr);
	} else {
		ret = data[addr];
	}
		
	return ret;
}

